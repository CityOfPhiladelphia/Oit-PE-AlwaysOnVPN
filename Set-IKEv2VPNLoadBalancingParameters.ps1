<#

.SYNOPSIS
    PowerShell script to increase the number of concurrent IPsec connections from the same source IP address.

.PARAMETER Restart
    Restarts the IKEEXT service.

.PARAMETER Connections
    Specify the number concurrent IPsec connections expected from a single source IP address. It is recommended to set this value to double the expected concurrent connections. The default value is 50,000.

.EXAMPLE
    .\Set-IKEv2LoadBalancingParameters.ps1

    Sets the number of concurrent IPsec connections expected from a single source IP address to 50,000 (the default value).

.EXAMPLE
    .\Set-IKEv2LoadBalancingParameters.ps1 -Connections 3000

    Sets the number of concurrent IPsec connections expected from a single source IP address to 3,000.

.EXAMPLE
    .\Set-IKEv2LoadBalancingParameters.ps1 -Restart

    Sets the number of concurrent IPsec connections expected from a single source IP address to 50,000 (the default value) and restarts the IKEEXT service.

.DESCRIPTION
    When a VPN server supporting IKEv2 is placed behind a load balancer the source IP address for the connection is often the load balancer, not the client's unique public IP address. By default, Windows is configured to expect a low number of connections from a single source IP address (10 by default). Running this script allows the administrator to adjust this parameter to a more suitable number. It is recommended to set this value to twice the number of expected concurrent IPsec connections. The default value is 50,000.

.LINK
    https://support.microsoft.com/en-gb/help/2579729/ipsec-traffic-may-be-blocked-when-a-computer-that-is-running-windows-7

